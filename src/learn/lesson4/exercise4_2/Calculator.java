package learn.lesson4.exercise4_2;

class Calculator {

    // cong so nguyen
    int add(int a, int b) {
        return a + b;
    }

    // cong so thuc
    double add(double a, double b) {
        return a + b;
    }

    // cong 3 so nguyen
    int add(int a, int b, int c) {
        return a + b + c;
    }
}
